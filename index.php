  <p>
    Counter-Strike (укр. Контрудар, скорочено CS; також відома як Half-Life: Counter-Strike або Counter-Strike 1.6) — багатокористувацька відеогра, шутер від першої особи, розроблений та випущений для Windows американською компанією Valve. Спочатку гра Counter-Strike являла собою користувацьку модифікацію до Half-Life, розроблену ентузіастами Мінем Ле і Джессом Кліффом в 1999 році; надалі Valve найняла обох розробників на роботу і придбала у них права інтелектуальної власності на гру, в 2000 році випустивши доопрацьовану версію Counter-Strike вже як самостійну гру. Гра була в різні роки портована на ряд платформ, у тому числі macOS, Linux[4] і Xbox (як окремої гри).
  </p>
  <p>
    Ігровий процес Counter-Strike проходить у форматі розбитого на кілька раундів матчу між двома командами гравців, одна з яких грає роль «терористів», а інша — контртерористичного спецназу; перед початком кожного матчу гравці вибирають «карту», на якій відбуватиметься бій. Команда може здобути перемогу в раунді, просто перебивши всіх супротивників або виконавши певні завдання, які можуть відрізнятися для різних карт. На початку кожного раунду гравці можуть купити зброю та допоміжне спорядження за ігрову валюту; за підсумками кожного раунду гравці отримують певну суму віртуальних грошей, які можуть використовувати для придбання більш потужної та ефективної зброї у наступному раунді.

Counter-Strike отримала надзвичайно високі відгуки критиків і набула величезної популярності серед гравців, у тому числі як одна з найбільш популярних кіберспортивних дисциплін у 2000-х роках. Надалі було випущено низку ігор-продовжень і спадкоємців, які складають серію Counter-Strike.

  </p>
  <p>
    Гравці в Counter-Strike поділяються на протиборчі команди терористів (T) і спецпризначенців (контртерористів, CT). Керуючи одним з бійців, гравець повинен забезпечити перемогу своєї команди, головним чином знищуючи опонентів упродовж кількох раундів.

Сутички відбуваються на місцевостях, званих картами. Кожен боєць володіє запасом здоров'я, початковою зброєю і коштами. За кошти можливо придбати іншу зброю, броню чи оснащення на кшталт приладу нічного бачення або щита. Гравцеві надаються відомості про стан здоров'я, броні, час до кінця раунду, кількість союзників, боєзапас і кошти. Також на екрані наводить радар, що показує розташування бійців, і відомості про те, хто кого вбив і з якої зброї.

На початку гравець обирає карту, команду і одного з чотирьох її бійців. Класи бійців косметичні — вони відрізняються тільки зовнішнім виглядом, але вигляд може сприяти маскуванню, залежно від карти. Команди починають сутичку, перебуваючи на «базі», навколо якої існує «зона покупок». Перебуваючи в ній, гравець може придбати за кошти корисні речі на свій розсуд. Раунд триває поки не буде досягнуто поставленої мети, або одна з команд не знищить іншу. В разі загибелі свого бійця, гравець продовжує спостерігати за сутичкою до кінця раунду. Коли раунд завершується, команди отримують кошти (переможці більшу суму) і в повному складі знову опиняються на своїх «базах» (респавняться).

  </p>

